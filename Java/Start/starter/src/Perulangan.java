public class Perulangan {
    //for 
    public static void main(String[] args){
        for (int i = 0; i <10; i++){
            System.out.println("nilainya: " + i);
        }

        //while 
    int i=0;
    while(i<10){
        System.out.println("nilainya:"+i);
        i++;
    }

    }
    
}
