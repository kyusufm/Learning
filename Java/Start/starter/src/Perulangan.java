public class Perulangan {
    //for 
    public static void main(String[] args){
        //for looping
        for (int i = 0; i <10; i++){
            System.out.println("nilainya: " + i);
        }

        //while looping
        int i=0;
        while(i<10){
            System.out.println("nilai while: "+i);
            i++;
        }

        //do while looping
        do{
            System.out.println("nilai abc: " + i);
            i++;
        }while(i<10);

    }
    
}
